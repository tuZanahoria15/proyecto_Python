"""
None is a special value in Python 
that represents the absence of a value or a null value. 

It is often used to indicate that a variable has no value 
or that a function does not return anything. 

The type of None is NoneType.
"""

x = None
print(x) #Prints None because x is assigned the value of None
print(type(x)) #Prints <class 'NoneType'> because x is of type NoneType


print(type("")) #Prints <class 'str'> because an empty string is of type str
print(type(0)) #Prints <class 'int'> because 0 is of type int
print(type(False)) #Prints <class 'bool'> because False is of type bool
print(type([])) #Prints <class 'list'> because an empty list is of type list
