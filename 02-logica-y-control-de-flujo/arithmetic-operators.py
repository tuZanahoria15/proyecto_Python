"""
Arithmetic Operators
are used to perform mathematical operations on numbers.
Here are the common arithmetic operators in Python:
"""
x = 5
y = 10

# +	Addition
print ("Addition: ", x + y)

# -	Subtraction
print ("Subtraction: ", x - y)

# *	Multiplication
print ("Multiplication: ", x * y)

# /	Division (returns a float)
print ("Division: ", x / y)

# %	Modulus (returns the remainder of the division)
print ("Modulus: ", x % y)

# **	Exponentiation
print ("Exponentiation: ", x ** y)

# //	Floor Division (returns the largest integer less than or equal to the division result)
print ("Floor Division: ", x // y)



# Is the number even or odd?
even = 8
print (even % 2 == 0) # True, because 8 is even

